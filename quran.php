 <h3>Surah As-Sajda</h3>
 <p>Surah As-Sajda, with 30 verses, is a powerful reminder of human creation, resurrection, and
 accountability. It’s often read during the Fajr prayer on Fridays.
 Reading Surah As-Sajda is a Sunnah of the Prophet Muhammad (peace be upon him) on Friday
 mornings, reinforcing its spiritual significance.</p>
 <h3>ٱلَّذِىٓ أَحْسَنَ كُلَّ شَىْءٍ خَلَقَهُۥ وَبَدَأَ خَلْقَ ٱلْإِنسَـٰنِ مِن طِينٍۢ ﴿٧﴾ ثُمَّ جَعَلَ نَسْلَهُۥ مِن سُلَـٰلَةٍۢ مِّن مَّآءٍۢ مَّهِينٍۢ ﴿٨﴾ ثُمَّ سَوَّىٰهُ وَنَفَخَ فِيهِ مِن رُّوحِهِۦ وَجَعَلَ لَكُمُ ٱلسَّمْعَ وَٱلْأَبْصَـٰرَ وَٱلْأَفْـِٔدَةَ ۚ قَلِيلًۭا مَّا تَشْكُرُونَ </h3>
 <p><i>"Who perfected everything He created and began the creation of man from clay. Then He
 madehis descendants from a fluid extract, despised. Then He proportioned him and breathed into
 him from His [created] soul and made for you hearing and vision and hearts; little are you grateful."
 </i></p>
 <h3>Surah Yaseen</h3>
 <p>Surah Yaseen, consisting of 83 verses, is known as the heart of the Qur’an. It’s recited for various
 purposes, including seeking blessings and protection.
Numerous Hadiths emphasize the virtues of Surah Yaseen, making it a popular Surah for regular
 recitation, especially on Fridays.</p>
 <h3>ءَايَةٌۭ لَّهُمُ ٱلْأَرْضُ ٱلْمَيْتَةُ أَحْيَيْنَـٰهَا وَأَخْرَجْنَا مِنْهَا حَبًّۭا فَمِنْهُ يَأْكُلُونَ ﴿٣٣﴾ وَجَعَلْنَا فِيهَا جَنَّـٰتٍۢ مِّن نَّخِيلٍۢ وَأَعْنَـٰبٍۢ وَفَجَّرْنَا فِيهَا مِنَ ٱلْعُيُونِ ﴿٣٤﴾ لِيَأْكُلُوا۟ مِن ثَمَرِهِۦ وَمَا عَمِلَتْهُ أَيْدِيهِمْ ۖ أَفَلَا يَشْكُرُونَ 
</h3>
 <p><i>"And a sign for them is the dead earth. We gave it life and brought forth from it grain, and from
 it they eat. And We placed therein gardens of palm trees and grapevines and caused springs to gush
 forth from it so they may eat of its fruit. And their hands did not produce it, so will they not be
 grateful?"
 </i></p>
