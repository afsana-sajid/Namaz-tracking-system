 <h2>When To Pray Tahajjud?</h2>
 <p>The Tahujjud prayer is the prayer of the night. The Tahajjud prayer is usually performed after Isha
 (which is the compulsory nightly prayer) and before performing Fajr (the mandatory morning prayer).
 Ideally, it is offered during the last third of the night, the time before Fajr. But it can also be offered
 anytime throughout the night after Isha. The last third of the night is a very blessed time.</p>
 <h2>Steps to Perform Tahajjud</h2>
 <p>Tahajjud prayers can be performed for two, four, six, eight, and twelve rak’ahs– but it can be
 done as much aspossible according to your ability. The number of rak’ahs is not limited, but at least
 two rak’ahs with one salutation (salam) for every two rak’ahs.</p>
 <ol>
 <li>After waking up from sleep in the middle of the night, make Wudu (ablution).</li>
 <li>The Sunnah Tahajjud prayer starts with reciting the Niyyah (Intention) to pray:</li>
 <h3>نَوَيْتُ أَنْ أُصَلِّيَ لِلَّهِ تَعَالَى رَكْعَتَيْنِ نَافِلَةَ التَّهَجُّدِ</h3>
 <li>Give the Takbir (Allahu-akbar) and perform a two raka’at prayer.</li>
 <li> End the prayer after two raka’at with salam.</li>
 <li> Add your own prayers (dua) after you’ve completed the number of rakats you set out to pray.
 Makesure that they are sincere, respectful, and done in complete devotion to Allah.</li>
 </ol>
 <h2>Dua to Recite During Tahajjud</h2>
 <blockquote>
 <h3>"اللَّهُمَّ اجْعَلْ فِي قَلْبِي نُورًا، وَفِي لِسَانِي نُورًا، وَفِي بَصَرِي نُورًا، وَفِي سَمْعِي نُورًا"</h3>
 <p>Translation: O Allah, place light in my heart, light on my tongue, light in my vision, and light
 in my hearing.</p>
 </blockquote>
 <h2>Is There Any Du’a For Tahajjud?</h2>
 <p>Upon making the Tahajjud, we may invoke Allah S.W.T. with any heartfelt supplications, even that
 which is never expressed and deeply reserved in our hearts. Dua is a special and exclusive
 conversation between us and our Lord.</p>
