    <p>Wudu, or ablution, is a ritual of purification performed by Muslims before engaging in prayer and specific acts of worship. It symbolizes both physical and spiritual cleanliness.</p>

    <h2>The Need for Ablution in Islam</h2>
    <p>Wudu is essential for prayer, as the Prophet (peace be upon him) said, "The key to Paradise is prayer, and the key to prayer is purity." If water is unavailable, <strong>tayamum</strong> can be performed as an alternative.</p>

    <h2>The Virtues of Wudu</h2>
    <div class="highlight">
        <p><strong>Qur'an:</strong> "O you who believe, when you intend to pray, wash your face, your hands up to the elbows, wipe your head, and wash your feet up to the ankles."</p>
        <p><strong>Hadith:</strong> "On the Day of Resurrection, my ummah will be called in such a state that their faces, hands, and feet will be shining with ablution."</p>
    </div>

    <h2>How to Perform Wudu Step-by-Step</h2>
    <img src="wazu.webp" alt="Wazu Steps">

    <h3>Step 1: Intention (Niyyah)</h3>
    <p>Begin with a sincere intention in your heart, acknowledging that you are performing Wudu for the sake of Allah.</p>

    <h3>Step 2: Washing the Hands</h3>
    <p>Recite <em>Bismillah</em> and wash both hands up to the wrists three times, ensuring water reaches between the fingers.</p>

    <h3>Step 3: Rinsing the Mouth</h3>
    <p>Take a handful of water into your mouth, swish it around, and expel it three times.</p>

    <h3>Step 4: Cleaning the Nostrils</h3>
    <p>Gently sniff water into your nostrils using your right hand and expel it with your left hand. Repeat three times.</p>

 <h3>Step 5: Washing the Face</h3>
 <p>Wash your face from the hairline to the chin and from ear to ear three times.</p>
 <h3>Step 6: Washing the Arms</h3>
 <p>Wash your right arm from the fingertips to the elbow three times. Repeat with the left
 arm.</p>
 <h3>Step 7: Wiping the Head</h3>
 <p>With wet hands, wipe your head once from the forehead to the back and then return to the
 front.</p>
 <h3>Step 8: Cleaning the Ears</h3>
 <p>Using wet fingers, clean inside and around your ears.</p>
 <h3>Step 9: Washing the Feet</h3>
 <p>Wash your feet up to the ankles three times, starting with the right foot. Ensure water
 reaches between the toes.</p>


    <h2>Dua After Wudu</h2>
    <blockquote>
        <h3>أَشْهَدُ أَنْ لَا إِلٰهَ إِلَّا اللَّهُ وَحْدَهُ لَا شَرِيكَ لَهُ، وَأَشْهَدُ أَنَّ مُحَمَّدًا عَبْدُهُ وَرَسُولُهُ</h3>
    </blockquote>

    <h2>Essentials of Wudu</h2>
    <ul>
        <li>Washing the face</li>
        <li>Washing both arms, including the elbows</li>
        <li>Wiping the head</li>
        <li>Washing both feet, including the ankles</li>
    </ul>
<h2>Conclusion</h2>
